import sys

input = sys.stdin.readline
N = int(input())
arr = [list(input().rstrip()) for _ in range(5)]
elevator = [[] for _ in range(N)]
for n in range(N):
    for i in range(5):
        for j in range(4 * n, 4 * n + 3):
            elevator[n].append(arr[i][j])

zero_nine = [['#', '#', '#', '#', '.', '#', '#', '.', '#', '#', '.', '#', '#', '#', '#'],
             ['.', '.', '#', '.', '.', '#', '.', '.', '#', '.', '.', '#', '.', '.', '#'],
             ['#', '#', '#', '.', '.', '#', '#', '#', '#', '#', '.', '.', '#', '#', '#'],
             ['#', '#', '#', '.', '.', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#'],
             ['#', '.', '#', '#', '.', '#', '#', '#', '#', '.', '.', '#', '.', '.', '#'],
             ['#', '#', '#', '#', '.', '.', '#', '#', '#', '.', '.', '#', '#', '#', '#'],
             ['#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '.', '#', '#', '#', '#'],
             ['#', '#', '#', '.', '.', '#', '.', '.', '#', '.', '.', '#', '.', '.', '#'],
             ['#', '#', '#', '#', '.', '#', '#', '#', '#', '#', '.', '#', '#', '#', '#'],
             ['#', '#', '#', '#', '.', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#']]

answer = [[] for _ in range(N)]
for num in range(N):
    basis = elevator[num]
    for i in range(10):
        if basis == zero_nine[i]:
            answer[num].append(i)
        for j in range(15):
            if basis[j] == '.' and zero_nine[i][j] == '#':
                answer[num].append(i)

print(answer)
